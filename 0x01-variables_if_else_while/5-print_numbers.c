#include <stdio.h>
#include <math.h>

/**
* main - pints base 10 numbers
* Return: Always 0
*/
int main(void)
{
	/* Define temporary variables */
	int value;

	/* For loop to generate the value of range 0 to 10 */
	for (value = 0; value < 10; value++)
	{
	/* Display result */
	printf("%d", value);
	}
	printf("\n");

	return (0);
}
